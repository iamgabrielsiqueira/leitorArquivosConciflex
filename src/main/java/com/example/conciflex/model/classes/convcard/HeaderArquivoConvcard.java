package com.example.conciflex.model.classes.convcard;

public class HeaderArquivoConvcard {
    private String tipoRegistro;
    private String nseqRegistro;
    private String versaoLayout;
    private String dataGeracao;
    private String horaGeracao;
    private String cnpjLojas;
    private String nomeLojas;
    private String espacoReservado;

    public String getTipoRegistro() {
        return tipoRegistro;
    }

    public void setTipoRegistro(String tipoRegistro) {
        this.tipoRegistro = tipoRegistro;
    }

    public String getNseqRegistro() {
        return nseqRegistro;
    }

    public void setNseqRegistro(String nseqRegistro) {
        this.nseqRegistro = nseqRegistro;
    }

    public String getVersaoLayout() {
        return versaoLayout;
    }

    public void setVersaoLayout(String versaoLayout) {
        this.versaoLayout = versaoLayout;
    }

    public String getDataGeracao() {
        return dataGeracao;
    }

    public void setDataGeracao(String dataGeracao) {
        this.dataGeracao = dataGeracao;
    }

    public String getHoraGeracao() {
        return horaGeracao;
    }

    public void setHoraGeracao(String horaGeracao) {
        this.horaGeracao = horaGeracao;
    }

    public String getCnpjLojas() {
        return cnpjLojas;
    }

    public void setCnpjLojas(String cnpjLojas) {
        this.cnpjLojas = cnpjLojas;
    }

    public String getNomeLojas() {
        return nomeLojas;
    }

    public void setNomeLojas(String nomeLojas) {
        this.nomeLojas = nomeLojas;
    }

    public String getEspacoReservado() {
        return espacoReservado;
    }

    public void setEspacoReservado(String espacoReservado) {
        this.espacoReservado = espacoReservado;
    }
}
